    private void tbinstitucionMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbinstitucionMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            //habilar los elementos para su modificacion

            try {
                frmbaremo form= new  frmbaremo();
                form.toFront();
                frmmenu.escritorio.add(form);
                form.setSize(920, 620);
                form.setVisible(true);
                int fila =tbinstitucion.rowAtPoint(evt.getPoint());
                frmbaremo.txtcodigoInstitucion.setText(tbinstitucion.getValueAt(fila, 0).toString());
                frmbaremo.txtnombreInstitucion.setText(tbinstitucion.getValueAt(fila, 1).toString());
                frmbaremo.btnActualizar.doClick();
                this.dispose();
            } catch (Exception e) {
                System.out.println(e);
                
            }
        }
    }//GEN-LAST:event_tbinstitucionMousePressed


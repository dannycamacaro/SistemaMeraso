    private void mostrar (String parametro,String buscar){
        try {
            DefaultTableModel modelo;
            finstitucion func= new finstitucion();
            modelo=func.mostrarInstitucion2("codigoInstitucion",buscar);
            tbinstitucion.setModel(modelo);
            lblTotal.setText(""+ Integer.toString(func.totalRegistros));   
            sizetable();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, "Error al cargar los registros en la tabla");
            JOptionPane.showConfirmDialog(rootPane, e);

        }

    }
    
        
    private void sizetable(){
        
        try {
            TableColumnModel columnModel = tbinstitucion.getColumnModel();
            columnModel.getColumn(0).setPreferredWidth(100);
            columnModel.getColumn(0).setMaxWidth(100);
            columnModel.getColumn(1).setPreferredWidth(700);
            columnModel.getColumn(1).setMaxWidth(700);
            columnModel.getColumn(2).setPreferredWidth(300);
            columnModel.getColumn(2).setMaxWidth(300);
            columnModel.getColumn(3).setPreferredWidth(800);
            columnModel.getColumn(3).setMaxWidth(800);
            columnModel.getColumn(4).setPreferredWidth(300);
            columnModel.getColumn(4).setMaxWidth(300);
            columnModel.getColumn(5).setPreferredWidth(400);
            columnModel.getColumn(5).setMaxWidth(400);
            columnModel.getColumn(6).setPreferredWidth(300);
            columnModel.getColumn(6).setMaxWidth(300);
            columnModel.getColumn(7).setPreferredWidth(300);
            columnModel.getColumn(7).setMaxWidth(300);
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, "Error al ajustar la tabla");
            JOptionPane.showConfirmDialog(rootPane, e);
        }
        
    }
    


    private void mostrar (String parametro,String buscar){
        try {
            DefaultTableModel modelo;
            String valor;
            String valorfinal;
            valorfinal="";
            valor= cbparametro.getSelectedItem().toString();
            if (valor=="CEDULA"){
                valorfinal="Cedula";
            }
            if (valor=="CODIGO"){
                valorfinal="codigoPaciente";
            }
            if (valor=="NOMBRE"){
                valorfinal="nombres";
            }
            if (valor=="APELLIDO"){
                valorfinal="apellidos";
            }
            System.out.println(cbparametro.getSelectedItem().toString());
            fpaciente func= new fpaciente();
            
            System.out.println(buscar);
            modelo=func.mostrarPaciente(valorfinal,buscar);
            tbpaciente.setModel(modelo);
            lblTotal.setText(""+ Integer.toString(func.totalRegistros));   
            sizetable();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
            JOptionPane.showConfirmDialog(rootPane, "Error al cargar los registros en la tabla.");
        }
 }
    
        
    private void sizetable(){
        try {
            TableColumnModel columnModel = tbpaciente.getColumnModel();
            columnModel.getColumn(0).setPreferredWidth(0);//codigo
            columnModel.getColumn(1).setPreferredWidth(100);//cedula
            columnModel.getColumn(2).setPreferredWidth(400);//nombre
            columnModel.getColumn(3).setPreferredWidth(400);//apellidos
            columnModel.getColumn(4).setPreferredWidth(0);//fecha nacimiento
            columnModel.getColumn(5).setPreferredWidth(0);//telefono
            columnModel.getColumn(6).setPreferredWidth(0);//direccion
            columnModel.getColumn(7).setPreferredWidth(0);//sexo
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al ajustar la tabla.");
        }
        
    }



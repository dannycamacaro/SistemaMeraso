    private void tbpacienteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbpacienteMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            //habilar los elementos para su modificacion
            try {
                int fila =tbpaciente.rowAtPoint(evt.getPoint());
                frmsolicitud.txtcodigoPaciente.setText(tbpaciente.getValueAt(fila, 0).toString());
                frmsolicitud.txtcedulaPaciente.setText(tbpaciente.getValueAt(fila, 1).toString());
                frmsolicitud.txtnombresPaciente.setText(tbpaciente.getValueAt(fila, 2).toString());
                frmsolicitud.txtapellidosPacientes.setText(tbpaciente.getValueAt(fila, 3).toString());
                frmsolicitud.jdfechaNacimiento.setDate(Date.valueOf(tbpaciente.getValueAt(fila, 4).toString()));
                frmsolicitud.txttelefonoPaciente.setText(tbpaciente.getValueAt(fila, 5).toString());
                frmsolicitud.txtdireccionPaciente.setText(tbpaciente.getValueAt(fila, 6).toString());
                if (tbpaciente.getValueAt(fila, 7).toString().equals("MASCULINO")){
                    frmsolicitud.cbsexo.setSelectedIndex(0);
                }
                else{
                    frmsolicitud.cbsexo.setSelectedIndex(1);

                }

                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error al tratar de importar los registros a los textfields.");
            }
        }
    }//GEN-LAST:event_tbpacienteMousePressed


    //Carga en una tabla los resutlados de la busqueda en instituciones
    private void mostrar (String parametro,String buscar){
        try {
            DefaultTableModel modelo;
            String valor;
            String valorfinal;
            valorfinal="";
            valor= cbparametro.getSelectedItem().toString();
            if (valor=="NOMBRE"){
                valorfinal="nombreInstitucion";
            }
            if (valor=="CODIGO"){
                valorfinal="codigoInstitucion";
            }
            if (valor=="CONTACTO"){
                valorfinal="contactoOrganizacion";
            }
            if (valor=="RIF"){
                valorfinal="rif";
            }
            System.out.println(cbparametro.getSelectedItem().toString());
            finstitucion func= new finstitucion();
            modelo=func.mostrarInstitucion2(valorfinal,buscar);
            tbinstitucion.setModel(modelo);
            lblTotal.setText(""+ Integer.toString(func.totalRegistros));   
            sizetable();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, "Error al cargar los registros en la tabla");
            JOptionPane.showConfirmDialog(rootPane, e);

        }

    }
    
        

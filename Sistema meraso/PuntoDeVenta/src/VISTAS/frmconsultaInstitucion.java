    private void tbinstitucionMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbinstitucionMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            //habilar los elementos para su modificacion

            try {
                int fila = tbinstitucion.rowAtPoint(evt.getPoint());
                frminstitucion.txtcodigoInstitucion.setText(tbinstitucion.getValueAt(fila, 0).toString());
                frminstitucion.txtnombreInstitucion.setText(tbinstitucion.getValueAt(fila, 1).toString());
                frminstitucion.txtcargoContacto.setText(tbinstitucion.getValueAt(fila, 2).toString());
                frminstitucion.txttelefono.setText(tbinstitucion.getValueAt(fila, 3).toString());
                frminstitucion.txtdireccion.setText(tbinstitucion.getValueAt(fila, 4).toString());
                frminstitucion.txtcontactoOrganizacion.setText(tbinstitucion.getValueAt(fila, 5).toString());
                frminstitucion.txtrif.setText(tbinstitucion.getValueAt(fila, 6).toString());
                frminstitucion.txttelefonoContacto.setText(tbinstitucion.getValueAt(fila, 7).toString());
                frminstitucion.btnhabilitar.doClick();
                this.dispose();
            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_tbinstitucionMousePressed

    private Connection connection= new conexion().conectar();
    private void btnreporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreporteActionPerformed
        Map p= new HashMap();
        JasperReport report;
        JasperPrint print;
        
        try {
            report=JasperCompileManager.compileReport(new File("").getAbsolutePath()+
            "/src/REPORTES/rptInstituciones.jrxml");
            
            print=JasperFillManager.fillReport(report, p,connection);
            JasperViewer view= new JasperViewer(print, false);
            view.setTitle("Reporte de Instituciones");
            view.setVisible(true);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnreporteActionPerformed


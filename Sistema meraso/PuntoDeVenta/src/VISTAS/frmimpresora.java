    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (txtcodigoImpresora.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Codigo para la Institucion");
            txtcodigoImpresora.requestFocus();
            return;
        }
        if (txtnombreImpresora.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre para la Institucion");
            txtnombreImpresora.requestFocus();
            return;
        }

        vimpresora dts= new vimpresora();
        fimpresora func=new fimpresora();

        dts.setCodigoImpresora(Integer.parseInt(txtcodigoImpresora.getText()));
        dts.setNombreImpresora(txtnombreImpresora.getText());
        dts.setDireccionImpresora(txtdireccion.getText());

        func.insertar(dts);
        mostrar("");
        inhabilitar();
        //habilitar botones
        btnmenu.setEnabled(true);
        btnnuevo.setEnabled(true);
        //ihabilitar botones
        btnguardar.setEnabled(false);

        //  sizetable();
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        if (txtcodigoImpresora.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Codigo para la impresora");
            txtcodigoImpresora.requestFocus();
            return;
        }

        if (txtnombreImpresora.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre para la Impresora");
            txtnombreImpresora.requestFocus();
            return;
        }

        
        if (txtdireccion.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una direccion para la impresora");
            txtdireccion.requestFocus();
            return;
        }
        vimpresora dts= new vimpresora();
        fimpresora func=new fimpresora();

        dts.setCodigoImpresora(Integer.parseInt(txtcodigoImpresora.getText()));
        dts.setNombreImpresora(txtnombreImpresora.getText());
        dts.setDireccionImpresora(txtdireccion.getText());
        func.modificar(dts);
        mostrar("");
        inhabilitar();
        btnmenu.setEnabled(true);
    }//GEN-LAST:event_btnmodificarActionPerformed


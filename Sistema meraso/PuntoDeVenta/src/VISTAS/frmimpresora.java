    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtcodigoImpresora.getText().equals("")) {

            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Seguro que quiere eliminar este registro?", "Confirmar", 2);

            if (confirmacion==0) {
                fimpresora func= new fimpresora();
                vimpresora dts= new vimpresora ();

                dts.setCodigoImpresora(Integer.parseInt(txtcodigoImpresora.getText()));
                func.eliminar(dts);
                //mostrar("");
                inhabilitar ();
                btnmenu.setEnabled(true);
                //sizetable();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed


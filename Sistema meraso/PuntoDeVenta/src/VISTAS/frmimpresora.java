      void inhabilitar (){
        txtnombreImpresora.setEnabled(false);
        txtdireccion.setEnabled(false);
        txtcodigoImpresora.setEnabled(false);
          
        //deshabilitar botones
        btnguardar.setEnabled(false);
        btneliminar.setEnabled(false);
        btnmodificar.setEnabled(false);
        btncancelar.setEnabled(false);
        
        //procedimiento limpiar cajas de texto
        limpiar();
        
    }
      
    private void sizetable(){
    TableColumnModel columnModel = tbimpresora.getColumnModel();
    columnModel.getColumn(0).setPreferredWidth(60);
    columnModel.getColumn(1).setPreferredWidth(150);
    columnModel.getColumn(2).setPreferredWidth(150);
    }

      void limpiar (){
          txtnombreImpresora.setText("");
          txtdireccion.setText("");
          txtcodigoImpresora.setText("");
      
      }
   
    void habilitar (){
          txtnombreImpresora.setEnabled(true);
          txtdireccion.setEnabled(true);
          txtcodigoImpresora.setEnabled(true);
          
          limpiar();
          
                  //deshabilitar botones
        btnguardar.setEnabled(true);
        btnmodificar.setEnabled(true);
        btncancelar.setEnabled(true);
        
    }
    
    
    void mostrar (String buscar){
        try {
            DefaultTableModel modelo;
            fimpresora func= new fimpresora();
            modelo=func.mostrarImpresora(buscar);
            tbimpresora.setModel(modelo);
   
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
            
        }
        
        sizetable();
    }

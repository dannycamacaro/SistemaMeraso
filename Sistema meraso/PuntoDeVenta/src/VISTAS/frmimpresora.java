    private void tbimpresoraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbimpresoraMouseClicked
        // TODO add your handling code here:
        //habilar los elementos para su modificacion
        btnmodificar.setEnabled(true);
        btneliminar.setEnabled(true);
        btncancelar.setEnabled(true);
        btnmenu.setEnabled(false);
        txtcodigoImpresora.setEnabled(false);
        txtnombreImpresora.setEnabled(true);
        txtdireccion.setEnabled(true);
        int fila =tbimpresora.rowAtPoint(evt.getPoint());

        txtcodigoImpresora.setText(tbimpresora.getValueAt(fila, 0).toString());
        txtnombreImpresora.setText(tbimpresora.getValueAt(fila, 1).toString());
        txtdireccion.setText(tbimpresora.getValueAt(fila, 2).toString());
    }//GEN-LAST:event_tbimpresoraMouseClicked

